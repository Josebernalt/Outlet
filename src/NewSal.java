    void generarSalida(){
        Logica lg = new Logica();
        String serie = lg.Nmro_salida();
        if (serie==null){
            TxtNumSa.setText("0000001");
        }else{
            int increment = Integer.parseInt(serie);
            increment = increment+1;
            TxtNumSa.setText("000000"+increment);
        }
    }

