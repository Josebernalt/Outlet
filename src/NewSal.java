    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked
        try {
            Cone con = new Cone();
            Connection cn = (Connection) con.conexion();
            if (TxtDesSa.getText().isEmpty()||jDateChooser1.getDate()== null|| TxtNumSa.getText().isEmpty()||TxtPreSa.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, """
                                              Atención:
                                              Por favor ingrese los valores solicitados""",  
                            "Error", JOptionPane.ERROR_MESSAGE);
            }else{
                int num = Integer.valueOf(TxtNumSa.getText());
                String des = TxtDesSa.getText();
                int prec = Integer.valueOf(TxtPreSa.getText());
                String fec = ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();
                PreparedStatement pst = cn.prepareStatement("INSERT INTO salida (número, descripción, precio, fecha)"
                    + "VALUES (?,?,?,?)") ;
                pst.setInt(1, num);
                pst.setString(2, des);
                pst.setInt(3, prec);
                pst.setString(4, fec);
                int n = pst.executeUpdate();
                if(n>0){
                    JOptionPane.showMessageDialog(null, "Se ha registrado los valores", "Ingreso", JOptionPane.INFORMATION_MESSAGE);
                    TxtDesSa.setText(null);
                    TxtNumSa.requestFocus();
                    TxtNumSa.setText(null);
                    TxtPreSa.setText(null);
                    jDateChooser1.setDate(null);
                    generarSalida();
            }
         }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al guardar"+ex, "Ingreso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabel1MouseClicked

